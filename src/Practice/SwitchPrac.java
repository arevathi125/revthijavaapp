package Practice;

public class SwitchPrac {
	public static void main(String[] args) {
		
		int games = 2;
		switch (games) {
		case 1:
			System.out.println("Football");
			break;
		case 2:
			System.out.println("Soccer");
		    break;
		case 3:
			System.out.println("Basket ball");
			break;
		case 4:
			System.out.println("Hockey");
			break;
		}
		
	}

}
