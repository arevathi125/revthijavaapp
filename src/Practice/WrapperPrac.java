package Practice;

public class WrapperPrac {

	public static void main(String[] args) {
		Integer a = 10;
		Double b = 44.55d;
		Character c = 's';
		String d = "yyy";
		System.out.println(a.intValue());
		System.out.println(b.doubleValue());
		System.out.println(c.charValue());
		System.out.println(d);
		System.out.println(d.length());

	}

}
